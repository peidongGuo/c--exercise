#include <iostream>
using namespace std;

int main() {
    string str;
    cin >> str;
    // 输出 Hello, World!
    cout << "Hello, World!"+str << endl;

    // 计算两个数的和
    int a = 5;
    int b = 10;
    int sum = a + b;

    // 输出结果
    cout << "The sum of " << a << " and " << b << " is: " << sum << endl;

    return 0;
}